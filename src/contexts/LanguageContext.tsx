export const LanguageProvider = ({ children }: LanguageProviderProps) => {
  // Mặc định là tiếng Tây Ban Nha, nhưng kiểm tra localStorage trước
  const [language, setLanguageState] = useState<Language>('es');

  // Kiểm tra và sử dụng ngôn ngữ đã lưu, hoặc mặc định là tiếng Tây Ban Nha
  useEffect(() => {
    // Đọc ngôn ngữ từ localStorage nếu có
    const savedLanguage = localStorage.getItem('language') as Language;
    const validLanguages: Language[] = ['vi', 'en', 'es'];
    
    if (savedLanguage && validLanguages.includes(savedLanguage)) {
      setLanguageState(savedLanguage);
    } else {
      // Nếu không có hoặc không hợp lệ thì đặt mặc định là tiếng Tây Ban Nha
      localStorage.setItem('language', 'es');
      setLanguageState('es');
    }
  }, []);

  // Lưu ngôn ngữ vào localStorage khi thay đổi
  const setLanguage = (lang: Language) => {
    localStorage.setItem('language', lang);
    setLanguageState(lang);
  };

  // Hàm dịch văn bản
  const t = (key: string, params?: Record<string, any>): string => {
    try {
      // Kiểm tra key hợp lệ
      if (typeof key !== 'string' || !key) {
        console.warn('Invalid translation key:', key);
        return '';
      }
      
      // Kiểm tra nếu không có bản dịch cho ngôn ngữ
      if (!translations[language]) {
        console.error(`No translations found for language: ${language}`);
        return key;
      }
      
      // Lấy chuỗi dịch hoặc trả về key nếu không tìm thấy
      let text = translations[language]?.[key];
      
      // Debug thông tin
      const isDebugMode = typeof window !== 'undefined' && new URLSearchParams(window.location.search).has('debug-translations');
      if (isDebugMode) {
        console.log(`[Translation] Language: ${language}, Key: ${key}, Found: ${Boolean(text)}, Value: ${text?.substring(0, 50) || 'undefined'}`);
      }
      
      // Nếu không tìm thấy bản dịch, sử dụng key
      if (text === undefined) {
        return key;
      }
      
      // Thay thế tham số nếu có
      if (params && typeof params === 'object' && Object.keys(params).length > 0) {
        Object.entries(params).forEach(([param, value]) => {
          const regex = new RegExp(`\\{${param}\\}`, 'g');
          const strValue = convertValueToString(value, param);
          text = text.replace(regex, strValue);
        });
      }
      
      return text;
    } catch (error) {
      console.error('Translation error:', error);
      return typeof key === 'string' ? key : '';
    }
  };
  
  // Hàm hỗ trợ chuyển đổi giá trị thành chuỗi an toàn
  function convertValueToString(value: any, paramName: string): string {
    if (value === undefined || value === null) {
      return '';
    }
    
    if (typeof value === 'object') {
      try {
        return String(value);
      } catch (err) {
        console.warn(`Error converting object param ${paramName}:`, err);
        return '';
      }
    }
    
    try {
      return String(value);
    } catch (err) {
      console.warn(`Error converting param ${paramName}:`, err);
      return '';
    }
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

// Hook để sử dụng context này
export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}; 